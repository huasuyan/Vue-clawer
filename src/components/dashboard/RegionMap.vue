  regionData: {
    type: Array,
    default: () => []
  }
})

const emit = defineEmits(['statTypeChange', 'dateChange', 'export'])

const statTypeOptions = [
  { label: '今天', value: 'daily' },
  { label: '近一周', value: 'weekly' },
  { label: '近一月', value: 'monthly' }
