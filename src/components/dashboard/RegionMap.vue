const mapChartRef = ref(null)
const activeStatType = ref('daily')
const startDate = ref(null)
const endDate = ref(null)
let mapChartInstance = null

const switchStatType = (type) => {
  activeStatType.value = type
  startDate.value = null
  endDate.value = null
  emit('statTypeChange', type)
}

const onDateChange = () => {
  emit('dateChange', {
    startDate: startDate.value,
    endDate: endDate.value
  })
}

const renderMap = () => {
  if (!mapChartRef.value) return
  if (mapChartInstance) mapChartInstance.dispose()
  mapChartInstance = echarts.init(mapChartRef.value)

  if (!props.regionData.length) {
    mapChartInstance.setOption({})
    return
  }

  const regionMap = {}
  props.regionData.forEach(item => {
    const name = mapToProvince(item.region)
    if (!name) return
    const value = Number(item.newsCount) || 0
    regionMap[name] = (regionMap[name] || 0) + value
  })
  const mapData = Object.entries(regionMap).map(([name, value]) => ({ name, value }))

  const option = {
    tooltip: {
      trigger: 'item',
      formatter: (params) => `${params.name}<br/>发文量: ${params.value || 0}`
    },
    visualMap: {
      min: 0,
      max: Math.max(...props.regionData.map(d => d.newsCount), 10),
      text: ['高', '低'],
      realtime: false,
      calculable: true,
      inRange: { color: ['#e0f3f8', '#abd9e9', '#74add1', '#4575b4', '#313695'] }
    },
    series: [{
      type: 'map',
      map: 'china',
      roam: true,
      label: { show: false, fontSize: 10 },
      emphasis: { label: { show: true } },
      data: mapData
    }]
  }

  mapChartInstance.setOption(option)
}

watch(() => props.regionData, async () => {
  await nextTick()
  renderMap()
}, { deep: true, immediate: true })

watch(activeStatType, () => {
  nextTick(() => renderMap())
})

onBeforeUnmount(() => {
  if (mapChartInstance) mapChartInstance.dispose()
})
